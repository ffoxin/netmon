#include "strutil.h"


/******************************************************************************/
/* Return true if char is delimiter (space or tab)                                                                           */
/******************************************************************************/
bool is_delim(const char *ch)
{
    return ((*ch == ' ') || (*ch == '\t'));
}

/******************************************************************************/
/* Return pointer to first non-delimiter char                                                                           */
/******************************************************************************/
const char * skip_delim(const char *buffer)
{
    while (is_delim(buffer))
    {
        buffer++;
    }

    return buffer;
}


/******************************************************************************/
/* Return pointer to first delimiter char                                                                           */
/******************************************************************************/
const char * skip_word(const char *buffer)
{
    while (!is_delim(buffer))
    {
        buffer++;
    }

    return buffer;
}

